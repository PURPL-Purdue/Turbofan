import numpy as np

def write(TF):
    with open("results.txt", "w") as txt:
        cycle = TF.cycle
        comp_LP = TF.compressor.LP
        turb_LP = TF.turbine.LP
        nozzle = TF.nozzle

        txt.write("####################################################\n")
        txt.write("#                  Cycle Analysis                  #\n")
        txt.write("####################################################\n")
        txt.write("\n======== General Parameters ========\n")
        txt.write("Thrust:\n")
        txt.write("    Specific Thrust                  | {:8.3f} N\n".format(cycle.OUT.ST))
        txt.write("    Total Thrust                     | {:8.3f} lbf\n".format(cycle.OUT.total_thrust))
        txt.write("    Core Thrust                      | {:8.3f} lbf\n".format(cycle.OUT.core_thrust))
        txt.write("    Bypass Thrust                    | {:8.3f} lbf\n".format(cycle.OUT.bypass_thrust))
        txt.write("    Bypass Thrust Percentage         | {:8.3f} %\n".format(cycle.OUT.bypass_thrust/cycle.OUT.total_thrust * 100))
        txt.write("Efficiencies:\n")
        txt.write("    Thrust Specific Fuel Consumption | {:8.5f} N\n".format(cycle.OUT.TSFC))
        txt.write("    Polytropic Efficiency            | {:8.5f} %\n".format(cycle.OUT.eta_p*100))
        txt.write("    Isentropic Efficiency            | {:8.5f} %\n".format(cycle.OUT.eta_th*100))
        txt.write("    Total Efficiency                 | {:8.5f} %\n".format(cycle.OUT.eta_0*100))
        txt.write("Pressure Ratios:\n")
        txt.write("    Fan Pressure Ratio               | {:8.2f}\n".format(cycle.IN.Pr.f))
        txt.write("    LPC Pressure Ratio               | {:8.2f}\n".format(cycle.IN.Pr.cLP))
        txt.write("    HPC Pressure Ratio               | {:8.2f}\n".format(cycle.IN.Pr.cHP))
        txt.write("    Total Pressure Ratio             | {:8.2f}\n".format(cycle.IN.Pr.f * cycle.IN.Pr.cLP * cycle.IN.Pr.cHP))
        txt.write("Misc:\n")
        txt.write("    Core Exit Velocity               | {:8.3f} m/s\n".format(cycle.OUT.u_ec))
        txt.write("    Bypass Exit Velocity             | {:8.3f} m/s\n".format(cycle.OUT.u_ef))
        txt.write("    Core Mass Flow Rate              | {:8.3f} kg/s\n".format(cycle.OUT.m_dot_core))
        txt.write("    Bypass Mass Flow Rate            | {:8.3f} kg/s\n".format(cycle.OUT.m_dot_bypass))
        txt.write("    Total Mass Flow Rate             | {:8.3f} kg/s\n".format(cycle.OUT.m_dot_total))
        txt.write("\n======== Station Pressures and Temperatures ========\n")
        txt.write("Temperatures:\n")
        txt.write("    T0a      | {:8.5f} K\n".format(cycle.OUT.T0P0.Sa.T0))
        txt.write("    T015     | {:8.5f} K\n".format(cycle.OUT.T0P0.S15.T0))
        txt.write("    T02      | {:8.5f} K\n".format(cycle.OUT.T0P0.S2.T0))
        txt.write("    T025     | {:8.5f} K\n".format(cycle.OUT.T0P0.S25.T0))
        txt.write("    T03      | {:8.5f} K\n".format(cycle.OUT.T0P0.S3.T0))
        txt.write("    T04      | {:8.5f} K\n".format(cycle.OUT.T0P0.S4.T0))
        txt.write("    T045     | {:8.5f} K\n".format(cycle.OUT.T0P0.S45.T0))
        txt.write("    T05      | {:8.5f} K\n".format(cycle.OUT.T0P0.S5.T0))
        txt.write("    T06      | {:8.5f} K\n".format(cycle.OUT.T0P0.S6.T0))
        txt.write("    T07      | {:8.5f} K\n".format(cycle.OUT.T0P0.S7.T0))
        txt.write("    T08      | {:8.5f} K\n".format(cycle.OUT.T0P0.S8.T0))
        txt.write("Pressures:\n")
        txt.write("    P0a      | {:8.5f} kPa\n".format(cycle.OUT.T0P0.Sa.P0/1000))
        txt.write("    P015     | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S15.P0/1000))
        txt.write("    P02      | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S2.P0/1000))
        txt.write("    P025     | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S25.P0/1000))
        txt.write("    P03      | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S3.P0/1000))
        txt.write("    P04      | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S4.P0/1000))
        txt.write("    P045     | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S45.P0/1000))
        txt.write("    P05      | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S5.P0/1000))
        txt.write("    P06      | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S6.P0/1000))
        txt.write("    P07      | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S7.P0/1000))
        txt.write("    P08      | {:8.5f} kPa\n".format(cycle.OUT.T0P0.S8.P0/1000))
        txt.write("\n\n")

        txt.write("####################################################\n")
        txt.write("#                Compressor Sizing                 #\n")
        txt.write("####################################################\n")
        txt.write("\n======== Compressor Information ========\n")
        txt.write("Ratios:\n")
        txt.write("    Total Pressure Ratio (design):    {:12.5f}\n".format(comp_LP.OUT.Pr_total))
        txt.write("    Total Pressure Ratio (actual):    {:12.5f}\n".format(comp_LP.OUT.Pr_total_actual))
        txt.write("    Total Temperature Ratio (design): {:12.5f}\n".format(comp_LP.OUT.Tr_total))
        txt.write("    Total Temperature Ratio (actual): {:12.5f}\n".format(comp_LP.OUT.Tr_total_actual))
        txt.write("Values:\n")
        txt.write("    Temperature Rise (total):         {:12.5f} K\n".format(comp_LP.OUT.temp_rise_total))
        txt.write("    Temperature Rise (per stage):     {:12.5f} K\n".format(comp_LP.OUT.temp_rise_per_stage))
        txt.write("    Pressure Rise (total):            {:12.5f} kPa\n".format(comp_LP.OUT.P0_rise_total/1000))
        txt.write("    Inlet Total Temperature (total):  {:12.5f} K\n".format(comp_LP.OUT.T0_stages[0]))
        txt.write("    Inlet Total Pressure (total):     {:12.5f} kPa\n".format(comp_LP.OUT.P0_stages[0]/1000))
        txt.write("    Exit Total Temperature (total):   {:12.5f} K\n".format(comp_LP.OUT.T0_stages[-1]))
        txt.write("    Exit Total Pressure (total):      {:12.5f} kPa\n".format(comp_LP.OUT.P0_stages[-1]/1000))
        txt.write("Aerodynamics:\n")
        txt.write("    Rotor Diffusion Factor:           {:12.5f}\n".format(comp_LP.OUT.stage_info["D_mr"]))
        txt.write("    Stator Diffusion Factor:          {:12.5f}\n".format(comp_LP.OUT.stage_info["D_ms"]))
        txt.write("    Stage Degree of Reaction:         {:12.5f}\n".format(comp_LP.OUT.stage_info["degR_m"]))
        txt.write("    De Haller Ratio Used:             {:12.5f}\n".format(comp_LP.IN.deHaller))
        txt.write("    Rotor Inlet Mach Number:          {:12.5f}\n".format(comp_LP.OUT.RVT.Mw_1m))
        txt.write("    Stator Inlet Mach Number:         {:12.5f}\n".format(comp_LP.OUT.RVT.Mc_2m))
        txt.write("    Compressor Inlet Mach Number:     {:12.5f}\n".format(comp_LP.OUT.RVT.Mc_1m))
        txt.write("    Compressor Outlet Mach Number:    {:12.5f}\n".format(comp_LP.OUT.RVT.Mz_3m))
        txt.write("Triangles:\n")
        txt.write("    Rotor Turning:                    {:12.5f} deg\n".format(abs(np.degrees(comp_LP.OUT.RVT.beta_2m  - comp_LP.OUT.RVT.beta_1m))))
        txt.write("    Stator Turning:                   {:12.5f} deg\n".format(abs(np.degrees(comp_LP.OUT.RVT.alpha_1m - comp_LP.OUT.RVT.alpha_2m))))
        txt.write("Misc:\n")
        txt.write("    Number of Stages (calculated):    {:12.5f}\n".format(comp_LP.OUT.num_stages_actual))
        txt.write("    Number of Stages (rounded up):    {:12.5f}\n".format(comp_LP.OUT.num_stages))
        txt.write("    Blade Chord Length:               {:12.5f} m\n".format(comp_LP.OUT.chord_m))
        txt.write("    RPM:                              {:12.5f} rpm\n".format(comp_LP.OUT.RPM))
        txt.write("    Inlet Tip Radius:                 {:12.5f} mm ({:.5f} in)\n".format(comp_LP.OUT.FF.r_tip_vec_full[0]*1000, comp_LP.OUT.FF.r_tip_vec_full[0]*1000/25.4))
        txt.write("    Inlet Tip Velocity:               {:12.5f} m/s\n".format(comp_LP.OUT.U_tip_inlet))
        RVT = comp_LP.OUT.RVT
        txt.write("Velocity Triangle:\n")
        txt.write("    C_1m: {:6.2f} ({:.3f}) | C_2m: {:6.2f} ({:.3f}) | C_3m: {:6.2f} ({:.3f}) | m/s (Mach)\n".format(RVT.C_1m, RVT.Mc_1m, RVT.C_2m, RVT.Mc_2m, RVT.C_3m, RVT.Mc_3m))
        txt.write("    W_1m: {:6.2f} ({:.3f}) | W_2m: {:6.2f} ({:.3f}) | W_3m: {:6.2f} ({:.3f}) | m/s (Mach)\n".format(RVT.W_1m, RVT.Mw_1m, RVT.W_2m, RVT.Mw_2m, RVT.W_3m, RVT.Mw_3m))
        txt.write("    U_1m: {:14.2f} | U_2m: {:14.2f} | U_3m: {:14.2f} | m/s\n".format(RVT.U_1m, RVT.U_2m, RVT.U_3m))
        txt.write("    alpha_1m: {:10.2f} | alpha_2m: {:10.2f} | alpha_3m: {:10.2f} | degrees\n".format(np.degrees(RVT.alpha_1m), np.degrees(RVT.alpha_2m), np.degrees(RVT.alpha_3m)))
        txt.write("    beta_1m: {:11.2f} | beta_2m: {:11.2f} | beta_3m: {:11.2f} | degrees\n".format(np.degrees(RVT.beta_1m), np.degrees(RVT.beta_2m), np.degrees(RVT.beta_3m)))

        txt.write("\n\n")
        txt.write("####################################################\n")
        txt.write("#                 Turbine Sizing                   #\n")
        txt.write("####################################################\n")
        txt.write("Overview:\n")
        txt.write("    Number of Stages:                 {:12d}\n".format(len(turb_LP.OUT.multistage_info)))
        txt.write("    Power Required by Compressor:     {:12.5f} MW\n".format(turb_LP.OUT.req_power_fan/1e6))
        txt.write("    Power Required by Fan:            {:12.5f} MW\n".format(turb_LP.OUT.req_power_comp/1e6))
        txt.write("    Total Power Required:             {:12.5f} MW\n".format(turb_LP.OUT.req_power/1e6))
        txt.write("    Total Power Generated:            {:12.5f} MW\n".format(turb_LP.OUT.total_power_gen/1e6))
        txt.write("    Excess Power Margin:              {:12.5f} %\n".format(turb_LP.OUT.excess_power_margin))
        txt.write("Info per Stage:\n")
        info = turb_LP.OUT.multistage_info
        tris = turb_LP.OUT.multistage_velocity_triangles
        for i in range(len(tris)):
            txt.write("    Stage {:d}\n".format(i+1))
            txt.write("        Power Generated:          {:12.5f} MW\n".format(info[i].power/1e6))
            txt.write("        Mean Degree of Reaction:  {:12.5f}\n".format(info[i].degR_m/1e6))
            txt.write("        --------------------------------------------------------------------------------\n")
            txt.write("        C_1m: {:6.2f} ({:.3f}) | C_2m: {:6.2f} ({:.3f}) | C_3m: {:6.2f} ({:.3f}) | m/s (Mach)\n".format(tris[i].C_1m, tris[i].Mc_1m, tris[i].C_2m, tris[i].Mc_2m, tris[i].C_3m, tris[i].Mc_3m))
            txt.write("        W_1m: {:6.2f} ({:.3f}) | W_2m: {:6.2f} ({:.3f}) | W_3m: {:6.2f} ({:.3f}) | m/s (Mach)\n".format(tris[i].W_1m, tris[i].Mw_1m, tris[i].W_2m, tris[i].Mw_2m, tris[i].W_3m, tris[i].Mw_3m))
            txt.write("        U_1m: {:14.2f} | U_2m: {:14.2f} | U_3m: {:14.2f} | m/s\n".format(tris[i].U_1m, tris[i].U_2m, tris[i].U_3m))
            txt.write("        alpha_1m: {:10.2f} | alpha_2m: {:10.2f} | alpha_3m: {:10.2f} | degrees\n".format(np.degrees(tris[i].alpha_1m), np.degrees(tris[i].alpha_2m), np.degrees(tris[i].alpha_3m)))
            txt.write("        beta_1m: {:11.2f} | beta_2m: {:11.2f} | beta_3m: {:11.2f} | degrees\n".format(np.degrees(tris[i].beta_1m), np.degrees(tris[i].beta_2m), np.degrees(tris[i].beta_3m)))
            txt.write("        --------------------------------------------------------------------------------\n")
            txt.write("        T0_1m: {:13.3f} | T0_2m: {:13.3f} | T0_3m: {:13.3f} | K\n".format(info[i].T0_1m, info[i].T0_2m, info[i].T0_3m))
            txt.write("        P0_1m: {:13.3f} | P0_2m: {:13.3f} | P0_3m: {:13.3f} | kPa\n".format(info[i].P0_1m/1e3, info[i].P0_2m/1e3, info[i].P0_3m/1e3))
        
        txt.write("\n\n")
        txt.write("####################################################\n")
        txt.write("#                  Nozzle Sizing                   #\n")
        txt.write("####################################################\n")
        txt.write("Core:\n")
        txt.write("    Exit Velocity:               {:12.5f} m/s\n".format(nozzle.OUT.exit_velocity_c))
        txt.write("    Exit Mach:                   {:12.5f}\n".format(nozzle.OUT.exit_mach_c))
        txt.write("    Exit Area:                   {:12.5f} m^2\n".format(nozzle.OUT.exit_area_c))
        txt.write("Bypass:\n")
        txt.write("    Exit Velocity:               {:12.5f} m/s\n".format(nozzle.OUT.exit_velocity_b))
        txt.write("    Exit Mach:                   {:12.5f}\n".format(nozzle.OUT.exit_mach_b))
        txt.write("    Exit Area:                   {:12.5f} m^2\n".format(nozzle.OUT.exit_area_b))

